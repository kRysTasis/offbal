<template>
    <div>
        <v-container
            fluid
            class="ma-0 pa-0 main_content_wrap"
            :class="{ 'is-task-drawer': drawer }"
        >
            <div class="search_result_header_wrap pl-5">
                <v-row>
                    <v-col
                        cols="6"
                        class="ma-0 pa-0"
                    >
                        <h3
                            class="ml-4 search_result_title"
                        >
                            Search Result
                        </h3>
                    </v-col>
                    <v-spacer></v-spacer>
                    <v-col
                        cols="6"
                        class="ma-0 pa-0"
                    >
                        <div class="operation_btn_wrap pl-10">
                            <v-spacer></v-spacer>
                            <FilterBtn
                                :isSearchResult=true
                            />
                            <SortBtn
                                :tasks=searchResult
                            />
                        </div>
                    </v-col>
                </v-row>
                <v-row>
                    <v-col
                        cols="12"
                        class="ma-0 pa-0"
                    >
                        <v-subheader>
                            {{ taskLength }} tasks
                        </v-subheader>
                    </v-col>
                </v-row>
                <div class="pb-2"></div>
                <v-divider></v-divider>
                <div class="pb-2"></div>
            </div>
            <div v-if="isLoading">
                <div ref="loadingContent" class="loading_div">
                </div>
            </div>
            <div v-else>
                <TaskList
                    :tasks=searchResult
                />
            </div>
        </v-container>
    </div>
</template>
<script>
    import TaskList from '@/components/common/TaskList'
    import FilterBtn from '@/components/parts/FilterBtn'
    import SortBtn from '@/components/parts/SortBtn'
    import Loading from '@/components/parts/Loading'

    import { mapGetters, mapMutations, mapActions } from 'vuex'

    export default {
        name: 'SearchResult',
        props: {
        },
        components: {
            TaskList,
            FilterBtn,
            SortBtn,
            Loading,
        },
        data: () => ({
            searchText: '',
            // searchResult: [],
            isLoading: false,
            drawer: false,
        }),
        created () {
            // FilterBtnから渡ってきた値で検索結果を絞る
            this.$eventHub.$off('filterSearchResult', this.filterSearchResult)
            this.$eventHub.$on('filterSearchResult', this.filterSearchResult)
        },
        mounted: function () {
        	this.$eventHub.$off('changeToggleDrawer', this.changeToggleDrawer)
        	this.$eventHub.$on('changeToggleDrawer', this.changeToggleDrawer)
            this.searchStart(this.$route.query.text)
        },
        watch: {
        },
        computed: {
            ...mapGetters([
                'searchResult',
            ]),
            taskLength () {
                // ロード中は0件を返す
                if (this.isLoading || this.searchResult === undefined) return 0
                return this.searchResult.length
            }
        },
        beforeRouteUpdate (to, from, next) {
            this.searchStart(to.query.text)
            next()
        },
        destroyed () {
            this.$eventHub.$off('filterSearchResult')
        },
        methods: {
            ...mapMutations([
                'setSearchResult',
            ]),
            ...mapActions([
                'getSearchResultAction',
                'getFilteredSearchResult',
            ]),
            search () {
                this.isLoading = true
                const loading = this.$vs.loading({
                    target: this.$refs.loadingContent,
                    scale: '0.6',
                    text: 'Loading...',
                    opacity: '0',
                })
                this.getSearchResultAction(this.searchText)
                .then(res => {
                    console.log('検索結果', res.data)
                    this.setSearchResult(res.data)
                    loading.close()
                    this.isLoading = false
                })
                .catch(e => {
                    loading.close()
                })
            },
            searchStart (text) {
                this.searchText = text
                this.search()
            },
            filterSearchResult (val) {
                // FilterBtnから渡ってきた値で検索結果を絞る
                const queryParams = {}
                queryParams.searchText = this.searchText
                for (const i in val) {
                    if (val[i] instanceof Array) {
                        if (val[i].length) {
                            queryParams[i] = val[i].join()
                        }
                    } else {
                        queryParams[i] = val[i]
                    }
                }
                this.getFilteredSearchResult(queryParams)
            },
            changeToggleDrawer (value) {
                this.drawer = value
            },
        }
    }
</script>
